# Membership Operator
# Membership operators are used to test if a sequence is presented in an object:
# in, not in
# if element in collection:
#     print("Yes")
# else:
#     print("No")
# Example:
# Check if "apple" is present in the fruits object:
fruits = ["apple", "banana"]
if "apple" in fruits:
    print("Yes, apple is a fruit!")
# Output: Yes, apple is a fruit!
# Example:
# Check if "cherry" is NOT present in the fruits object:
fruits = ["apple", "banana"]
if "cherry" not in fruits:
    print("Yes, cherry is NOT a fruit!")